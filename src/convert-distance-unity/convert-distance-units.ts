/**
 * convert-distance-units
 */
//convert kilometer to meter
function convertKilometerToMeter(km: number): number {
  return km * 1000;
}
//convert kilometer to centimeter
function convertKilometerToCentimeter(km: number): number {
  return km * 100000;
}
//convert kilometer to millimeter
function convertKilometerToMillimeter(km: number): number {
  return km * 1000000;
}
//convert kilometer to micrometer
function convertKilometerToMicrometer(km: number): number {
  return km * 1000000000;
}
//convert kilometer to nanometer
function convertKilometerToNanometer(km: number): number {
  return km * 1000000000000;
}
//convert kilometer to picometer
function convertKilometerToPicometer(km: number): number {
  return km * 1000000000000000;
}
//convert kilometer to femtometer
function convertKilometerToFemtometer(km: number): number {
  return km * 1000000000000000000;
}
//convert kilometer to attometer
function convertKilometerToAttometer(km: number): number {
  return km * 1000000000000000000000;
}
//convert kilometer to zeptometer
function convertKilometerToZeptometer(km: number): number {
  return km * 1000000000000000000000000;
}
//convert mile to meter
function convertMileToMeter(mile: number): number {
  return mile * 1609.34;
}
//convert mile to centimeter
